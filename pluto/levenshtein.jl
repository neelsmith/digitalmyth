md"""###### In the 'StringDistances' package we can measure the 'distance' between two strings i.e. the amount you would have to change the first string to make a second string. This is known as the *edit distance* function. 

###### The Levenshtein edit distance however is the number of changes you would make to create a singular string from the first or other. In this, you would delete or add a single letter to change it.

###### We use *normalized* value to take the account of the length of the actual words to score an edit distance on the scale from 0 to 1.

	1.0 meaning the words are Identical!
	0.0 meaning the words have no letters or characters in common!

"""

